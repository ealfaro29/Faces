import { useEffect } from 'react';

export const FAVICONS = {
  HEEL: 'data:image/svg+xml,<svg xmlns=%22http://www.w3.org/2000/svg%22 viewBox=%220 0 100 100%22><text y=%22.9em%22 font-size=%2290%22>👠</text></svg>',
  CROWN: 'data:image/svg+xml,<svg xmlns=%22http://www.w3.org/2000/svg%22 viewBox=%220 0 100 100%22><text y=%22.9em%22 font-size=%2290%22>👑</text></svg>',
};

export function useFavicon(iconUri) {
  useEffect(() => {
    const link = document.querySelector("link[rel*='icon']") || document.createElement('link');
    link.type = 'image/svg+xml';
    link.rel = 'shortcut icon';
    link.href = iconUri;
    document.getElementsByTagName('head')[0].appendChild(link);
  }, [iconUri]);
}
